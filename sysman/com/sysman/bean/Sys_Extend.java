package com.sysman.bean;

import com.base.bean.IDBean;

public class Sys_Extend extends IDBean{

	public String ref_table_name;  //关联表
	public String ref_id; //关联ID
	public String field_no;//扩展字段名称
	public String field_value; //扩展字段值
	public String remark; //备注
	

	public Sys_Extend(String ref_table_name , String ref_id, String field_id , 
			String field_value, String remark,String id) {
		super();
		this.id = id ; 
		this.ref_table_name = ref_table_name;
		this.ref_id = ref_id;
		this.field_no = field_id;
		this.field_value = field_value;
		this.remark = remark;
	}

	
}
